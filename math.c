
/*
Objective : find max between two value
Input : int a, int b
Output : int
*/
int int_max(int a, int b){
    if (a > b){
        return  a;
    }
    return b;
}

/*
Objective : find min between two value
Input : int a, int b
Output : int
*/
int int_min(int a, int b){
    if (a < b){
        return  a;
    }
    return b;
}
